
public class SquareOfBigAlphabets{

    public static void main(String[] args) {
        int n=4;
        for (int i = 1; i <= n; i++) {
            for (char j = 65; j <= 68; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}


